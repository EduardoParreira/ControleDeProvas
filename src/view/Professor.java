    private void gravarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_gravarActionPerformed
        if (!this.nomeProfessor.getText().equals("")) {
            ControleProfessor controleProfessor = new ControleProfessor();
            ProfessorBEAN professor = new ProfessorBEAN(this.nomeProfessor.getText(), status);
            controleProfessor.addProfessor(professor);
            JOptionPane.showMessageDialog(null, "Professor cadastrado com sucesso!");
            this.nomeProfessor.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "Preencha o nome de cadastrar");
        }
    }//GEN-LAST:event_gravarActionPerformed

