    private void excluirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_excluirActionPerformed
        if (!this.nomeProfessor.getText().equals("") || !this.codigoProfessor.getText().equals("")) {
            int idProfessor = Integer.parseInt(this.codigoProfessor.getText());
            ControleProfessor controleProfessor = new ControleProfessor();
            ProfessorBEAN professor = new ProfessorBEAN(idProfessor, this.nomeProfessor.getText(), status);
            controleProfessor.deleteProfessor(idProfessor);
            JOptionPane.showMessageDialog(null, "Professor excluído com sucesso!");
        } else {
            JOptionPane.showMessageDialog(null, "Preencha o código antes de excluir");
        }
    }//GEN-LAST:event_excluirActionPerformed

