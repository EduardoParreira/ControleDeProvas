    private void alterarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_alterarActionPerformed
        if (!this.nomeProfessor.getText().equals("") || !this.codigoProfessor.getText().equals("")) {
            int idProfessor = Integer.parseInt(this.codigoProfessor.getText());
            ControleProfessor controleProfessor = new ControleProfessor();
            ProfessorBEAN professor = new ProfessorBEAN(idProfessor, this.nomeProfessor.getText(), status);

            controleProfessor.updateProfessor(professor);

            JOptionPane.showMessageDialog(null, "Professor alterado com sucesso!");

            this.nomeProfessor.setText("");

        } else {
            JOptionPane.showMessageDialog(null, "Preencha o código antes de alterar");
        }

    }//GEN-LAST:event_alterarActionPerformed

